package modelo;

public class Produto {
	
	private String nome;
	private long codBarras;
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public long getCodBarras() {
		return codBarras;
	}
	
	public void setCodBarras(long l) {
		this.codBarras = l;
	}
}